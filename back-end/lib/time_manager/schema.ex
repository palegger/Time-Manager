  def sign_in(username, password) do
    query =
      from user in User,
        where: user.username == ^username and user.password == ^Pbkdf2.Base.hash_password(password, "a")
    # IO.puts(Pbkdf2.Base.hash_password("dadz", "a"))
    Repo.one(query)
  end
